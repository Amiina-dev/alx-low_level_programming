/**
 * add - add
 * @a: int
 * @b: int
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract
 * @a: integer
 * @b: integer
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * divide - divide
 * @a: int
 * @b: int
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod
 * @a: int
 * @b: int
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
